public class TiposPrimitivos {
  static void main(String[] args) {
    // Tipos primitivos do java --> int , double , float , char , byte , short , long , boolean//

    String nome = "Bruno";
    double nota = 8.9;
    double frequencia = 68.2;
    char sala = 'A';
    char caractere = 65;
    int n1 = 15;
    byte quantidadedebyte = 127;


    System.out.println("O nome do aluno é: " + nome);
    System.out.println("A média final da nota é: " + nota);
    System.out.println("A média de frequencia do aluno é: " + frequencia);
    System.out.println("A sala de aula é: Sala " + sala);
    System.out.println("O número é: " + n1);
    System.out.println("Limite de byte: " + quantidadedebyte);
    System.out.println("char "+ caractere);


  }
}
